                           <h3>História</h3>



<p>E no dia 1º de Maio desse ano, no bota-abaixo do mesmo moliceiro, actuou um agrupamento de 8 elementos: Manuel de Matos, Marques da Costa, João Rilho, Joaquim M. Silva, Manuel Tavares, Salvador Pardaleiro, Joaquim Fonseca e José Lopes, onde tocaram a marcha do trabalhador, após um encontro na taberna do Fateixas.</p>


<p>Porém, vencidas as primeiras e todas as dificuldades que foram surgindo, decidiram os valentes fundadores apetrechar o grupo de estatutos, órgãos sociais e até um novo nome, que julgavam mais adequado ao presente e encorajador para futuro que queriam promissor.</p>

<p>É nestas circunstâncias que se realiza a Assembleia Geral Constitutiva, em 19 de Outubro de 1930, com António Joaquim de Rezende como Presidente, secretariado pelo Prof. José Teixeira dos Reis e pelo Dr. António Emílio Bastos de Abreu Freire, onde ficou decido a substituição do nome provisório, Grupo União Pardilhoense em Capricho pelo de Associação Musical Pardilhoense Saavedra Guedes, em homenagem a um homem que foi professor de muitos fundadores desta Associação e um caprichoso e combativo Pardilhoense por adopção. </p>


